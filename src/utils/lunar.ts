export function lunarToSolar(year: number, month: number, day: number): Date {
  const baseYear = 1900;
  const baseMonth = 1;
  const baseDay = 31;
  
  let solarYear = year;
  let solarMonth = month;
  let solarDay = day;
  
  const leapMonth = getLeapMonth(year);
  const isLeap = month === leapMonth;
  
  const daysInYear = getDaysInLunarYear(year);
  const daysInMonth = getDaysInLunarMonth(year, month, leapMonth);
  
  let dayOfYear = 0;
  for (let i = 1; i < month; i++) {
    dayOfYear += getDaysInLunarMonth(year, i, leapMonth);
  }
  dayOfYear += day;
  
  let totalDays = 0;
  for (let y = baseYear; y < year; y++) {
    totalDays += getDaysInLunarYear(y);
  }
  totalDays += dayOfYear;
  
  const baseDate = new Date(baseYear, baseMonth - 1, baseDay);
  baseDate.setDate(baseDate.getDate() + totalDays - 1);
  
  solarYear = baseDate.getFullYear();
  solarMonth = baseDate.getMonth() + 1;
  solarDay = baseDate.getDate();
  
  if (isLeap && leapMonth !== 0) {
    const prevMonthDays = getDaysInLunarMonth(year, month - 1, leapMonth);
    if (day > prevMonthDays) {
      baseDate.setDate(baseDate.getDate() + getDaysInLunarMonth(year, month, leapMonth));
      solarYear = baseDate.getFullYear();
      solarMonth = baseDate.getMonth() + 1;
      solarDay = baseDate.getDate();
    }
  }
  
  return new Date(solarYear, solarMonth - 1, solarDay);
}

function getLeapMonth(year: number): number {
  const index = year - 1900;
  if (index < 0 || index >= SOLAR_TERMS.length) return 0;
  return (SOLAR_TERMS[index] >> 16) & 0xf;
}

function getDaysInLunarYear(year: number): number {
  const index = year - 1900;
  if (index < 0 || index >= SOLAR_TERMS.length) return 354;
  return SOLAR_TERMS[index] & 0xffff;
}

function getDaysInLunarMonth(year: number, month: number, leapMonth: number): number {
  const index = year - 1900;
  if (index < 0 || index >= SOLAR_TERMS.length) return 29;
  
  const yearData = SOLAR_TERMS[index];
  const leapMonthVal = (yearData >> 16) & 0xf;
  
  let days = 29;
  const bit = month;
  
  if ((yearData & (1 << bit)) !== 0) {
    days = 30;
  }
  
  if (month === leapMonthVal && leapMonthVal !== 0) {
    if ((yearData & (1 << (bit + 12))) !== 0) {
      days = 30;
    } else {
      days = 29;
    }
  }
  
  return days;
}

export function getNextBirthday(lunarMonth: number, lunarDay: number): Date {
  const now = new Date();
  const currentYear = now.getFullYear();
  
  let targetDate = lunarToSolar(currentYear, lunarMonth, lunarDay);
  
  if (targetDate < now) {
    targetDate = lunarToSolar(currentYear + 1, lunarMonth, lunarDay);
  }
  
  return targetDate;
}

